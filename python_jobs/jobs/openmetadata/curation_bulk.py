def apply_all_curation():
    """Apply curation to all mart and raw tables."""
    print("Starting catalog curation...")
    all_tables = MART_CURATION + RAW_CURATION
    for i, table in enumerate(all_tables, 1):
        print(f"[{i}/{len(all_tables)}] Curating: {table['fqn']}")
        try:
            patch_table(
                fqn=table["fqn"],
                description=table.get("description"),
                owner_name=table.get("owner"),
                tags=table.get("tags"),
                tier=table.get("tier"),
            )
        except Exception as e:
            print(f"⚠️  Failed: {table['fqn']} — {e}")
    print(f"\n✅ Curation complete. Processed {len(all_tables)} tables.")


if __name__ == "__main__":
    apply_all_curation()

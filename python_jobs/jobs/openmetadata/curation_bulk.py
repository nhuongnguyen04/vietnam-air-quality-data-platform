"""
Bulk catalog curation using OM REST API.
No openmetadata-ingestion library needed — uses simple requests calls.

Usage:
    OM_URL=http://openmetadata:8585/api \
    OM_USER=admin@open-metadata.org \
    OM_PASS=admin \
    python python_jobs/jobs/openmetadata/curation_bulk.py

Environment variables:
    OM_URL: OpenMetadata API base URL
    OM_USER: OM admin username
    OM_PASS: OM admin password
"""

import os
import sys
import requests

OM_URL = os.environ.get('OM_URL', 'http://openmetadata:8585/api')
OM_USER = os.environ.get('OM_USER', 'admin@open-metadata.org')
OM_PASS = os.environ.get('OM_PASS', 'admin')


def om_login() -> str:
    """Login to OM and return JWT token."""
    r = requests.post(
        f"{OM_URL}/v1/users/login",
        json={"username": OM_USER, "password": OM_PASS},
        headers={"Content-Type": "application/json"},
        timeout=30,
    )
    r.raise_for_status()
    return r.json()["token"]


def patch_table(fqn: str,
                description: str = None,
                owner_name: str = None,
                tags: list = None,
                tier: str = None) -> None:
    """Apply curation to a table entity via JSON Patch (RFC 6902)."""
    token = om_login()
    headers = {
        "Content-Type": "application/json",
        "Authorization": f"Bearer {token}",
    }

    patch_ops = []

    if description:
        patch_ops.append({
            "op": "replace",
            "path": "/description",
            "value": description,
        })

    if owner_name:
        user_resp = requests.get(
            f"{OM_URL}/v1/users/name/{owner_name}",
            headers=headers,
            timeout=15,
        )
        if user_resp.ok:
            user_id = user_resp.json()["id"]
            patch_ops.append({
                "op": "replace",
                "path": "/owner",
                "value": {"id": user_id, "type": "user"},
            })

    if tags:
        patch_ops.append({
            "op": "replace",
            "path": "/tags",
            "value": [
                {"tagFQN": f"Data.{t}", "labelType": "Manual", "state": "Confirmed"}
                for t in tags
            ],
        })

    if tier:
        patch_ops.append({
            "op": "replace",
            "path": "/tier",
            "value": {"tagFQN": f"Tier.{tier}"},
        })

    if not patch_ops:
        return

    fqn_url = fqn.replace('.', '/')
    r = requests.patch(
        f"{OM_URL}/v1/tables/name/{fqn_url}",
        headers=headers,
        json=patch_ops,
        timeout=30,
    )
    if r.status_code == 404:
        print(f"⚠️  Table not found: {fqn} (skipping)")
        return
    r.raise_for_status()
    print(f"✅ Curated: {fqn}")


